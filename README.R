#Project: Estimating RMST
#Description:
#This repository contains examples of code for estimating RMST using primarily 
#parametric methods.The code uses data from the survival package in R readily 
#available to anyone.The code was drafted as part of a project to compare 
#extrapolated survival to empirical survival and adjusted accordingly for the R 
#HTA conference in May 19th-23rd 2022

#The code includes several functions to fit parametric models, plot extrapolations 
#and estimate RMST. The code also includes an estimate on the 'true' KM estimate.

